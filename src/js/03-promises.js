const formRef = document.querySelector('form');

let intervalId = null;
let timeoutId = null;
let position = 0;
let delay = 0;

function createPromise(position, delay) {
  const shouldResolve = Math.random() > 0.3;
  return new Promise((resolve, reject) => {
    if (shouldResolve) {
      resolve({ position, delay });
    } else {
      reject({ position, delay });
    }
  })
    .then(({ position, delay }) => {
      console.log(`✅ Fulfilled promise ${position} in ${delay}ms`);
      Notiflix.Notify.success(`✅ Fulfilled promise ${position} in ${delay}ms`);
    })
    .catch(({ position, delay }) => {
      console.log(`❌ Rejected promise ${position} in ${delay}ms`);
      Notiflix.Notify.failure(`❌ Rejected promise ${position} in ${delay}ms`);
    });
}

function createInterval(step, amount) {
  intervalId = setInterval(() => {
    createPromise(position, delay);

    if (position >= parseInt(amount)) {
      clearInterval(intervalId);
      return;
    }
    delay += parseInt(step);
    position += 1;
  }, parseInt(step));
}

function handlerForm(e) {
  e.preventDefault();
  clearInterval(intervalId);
  clearTimeout(timeoutId);
  delay = 0;
  position = 0;
  const formData = new FormData(e.target);
  const formValues = Object.fromEntries(formData.entries());
  const amount = parseInt(formValues.amount);
  const step = parseInt(formValues.step);
  delay = parseInt(formValues.delay);

  if (!amount || amount <= 0) {
    Notiflix.Notify.warning(`Amount must be greater than 0`);
    return;
  }
  timeoutId = setTimeout(() => {
    createInterval(step, amount);
    position += 1;
  }, delay);
  // formRef.reset();
}

formRef.addEventListener('submit', handlerForm);
